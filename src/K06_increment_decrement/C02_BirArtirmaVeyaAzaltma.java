package K06_increment_decrement;

public class C02_BirArtirmaVeyaAzaltma {

    public static void main(String[] args) {



        int a=20;
        int b=40;

        a++;

        System.out.println("12.satırdaki a değeri : "+a); //21

        ++a;

        System.out.println("16. satırdaki a değeri : " +a); //22

        b--;

        System.out.println("20. Satırdaki b değeri : " + b); //39

        --b;

        System.out.println("24. satırdaki b değeri : " + b); //38





    }
}
