package K15_doWhileLoop;

public class C01_whileLoopVsDoWhileLoop {

    public static void main(String[] args) {

        /*
        while loop da condition kontrolü
        loop body'den daha fazla çalışır
         */

        //1'den 5'e kadar 5 hariç olan sayıları yazdırın


        int sayi =1;

        while (sayi<5){
            System.out.print(sayi+" ");
            sayi++;
        }

        System.out.println(" ");

    }
}
