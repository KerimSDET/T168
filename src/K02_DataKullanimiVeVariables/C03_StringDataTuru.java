package K02_DataKullanimiVeVariables;

public class C03_StringDataTuru {

    static void main() {

        String str = "Java Candır";

        int sayi = 20;


        System.out.println(str);

        //Non primitive data türlerinin HAZIR METHOD'ları olur

        System.out.println(str.toLowerCase()); //java candır yazar

        System.out.println(str.toUpperCase()); //JAVA CANDIR

        System.out.println(sayi);

    }
}
